class DynamicList(list):
    def __init__(self, gen):
        self._gen = gen

    def __getitem__(self, index):
        while index >= len(self):
            self.append(self._gen)
        return super(DynamicList, self).__getitem__(index)

def timediff(
        start: datetime, 
        end: datetime, 
        start_watching: Optional[datetime] = datetime(1970, 1, 1, 0, 0, 0),
        end_watching: Optional[datetime] = datetime(9999, 12, 31, 23, 59, 59)
    ) -> int:
    if start_watching > end:
        return 0
    if end_watching < start:
        return 0
    
    adjusted_start = start if start > start_watching else start_watching
    adjusted_end = end if end < end_watching else end_watching
    return (adjusted_end - adjusted_start).total_seconds()

def extract_active_ids(
        els: List[Union[datetime, Tuple[datetime, int]]]
    ) -> List[int]:
    result = dict()
    for element in els:
        id = 1
        event = element
        if isinstance(element, tuple):
            event, id = element
        if id not in result.keys():
            result[id] = 0
        result[id] += 1
    return result

def close_event_loops(
        els: List[Union[datetime, Tuple[datetime, int]]],
        active_ids: List[int],
        end_watching: Optional[datetime] = datetime(9999, 12, 31, 23, 59, 59),
    ) -> List[Union[datetime, Tuple[datetime, int]]]:
    for id, count in active_ids.items():
        if not count % 2 == 0:
            els.append((end_watching, id))
    return els

def sort_events(
        els: List[Union[datetime, Tuple[datetime, int]]]
    ) -> List[Union[datetime, Tuple[datetime, int]]]:
    events = dict()
    for element in els:
        id = 1
        event = element
        if isinstance(element, tuple) or isinstance(element, list):
            event, id = element
        if event not in events.keys():
            events[event] = list()
        events[event].append(id)
    return sorted([(k, v) for k,v in events.items()])

def room_light(
        lights_on: List[int],
        req: Optional[int] = 1
    ) -> int:
    return sum(lights_on) >= req


def normalize_events(
        els: List[Union[datetime, Tuple[datetime, int]]],
        operating: timedelta,
        end_watching: Optional[datetime] = datetime(9999, 12, 31, 23, 59, 59),
        req: Optional[int] = 1
    ) -> List[Tuple[datetime, int]]:
    normalized_els = list()
    lights_on = DynamicList(False)
    lights_remain = DynamicList(operating)
    event_prior = DynamicList(None)
    room_before = False
    
    active_ids = extract_active_ids(els)
    els = close_event_loops(els, active_ids, end_watching)
    els = sort_events(els)
    
    debugprint(f'Lights are now {lights_on}, room is {room_before}')
    for element in els:
        event, ids = element
        debugprint(f'Processing event {event} for {ids}')
        # initializes the light if it wasn't already:
        for id in ids:
            _ = (lights_on[id], lights_remain[id], event_prior[id])
        
        # advance dead timers on all working lights
        for lid, remain in enumerate(lights_remain):
            debugprint(f'Advance {lid}, {remain}')
            if event_prior[lid] is None:
                # was not turned on before, so we just record this
                for id in ids:
                    if id == lid:
                        debugprint(f'No prior events for {lid}, turning on')
                        event_prior[lid] = event
                continue
            if remain.total_seconds() <= 0:
                # dead, nothing to advance
                debugprint(f'Dead timer expired for {lid}')
                continue
            if lights_on[lid]:
                # was on, advance the timer
                elapsed = (event - event_prior[lid])
                debugprint(f'Light {lid} was on {elapsed} of {remain}')
                if elapsed > remain:
                    debugprint(f'Light {lid} is expired in-flight')
                    # the light died in-flight
                    lights_on[lid] = False
                    # check if we need to inject an event into normalizer:
                    if not room_light(lights_on, req):
                        # the room became dark N seconds ago
                        f_event = event_prior[lid] + remain
                        debugprint(f'Light {lid} left the room dark, injecting event {f_event}')
                        normalized_els.append(f_event)
                        room_before = room_light(lights_on, req)
                    # zero-out remaining:
                    lights_remain[lid] = timedelta.min
                elif elapsed == remain:
                    debugprint(f'Light {lid} expires now')
                    lights_on[lid] = False
                    lights_remain[lid] = timedelta.min
                else:
                    lights_remain[lid] -= elapsed
                event_prior[lid] = event
        
        # toggle the light:
        for id in ids:
            if lights_remain[id] > timedelta.min:
                lights_on[id] ^= True
        
        # if any of the lights are on, the event has no effect on the room
        if not room_light(lights_on, req) == room_before:
            normalized_els.append(event)
            room_before = room_light(lights_on, req)
        debugprint(f'Lights are now {lights_on}, room is {room_before}')
        
    return normalized_els


def sum_light(
        els: List[Union[datetime, Tuple[datetime, int]]],
        start_watching: Optional[datetime] = datetime(1970, 1, 1, 0, 0, 0),
        end_watching: Optional[datetime] = datetime(9999, 12, 31, 23, 59, 59),
        operating: Optional[timedelta] = None,
        req: Optional[int] = 1
    ) -> int:
    """
        how long the light bulb has been turned on
    """
    if operating is None:
        operating = (datetime(9999, 12, 31, 23, 59, 59) - datetime(1970, 1, 1, 0, 0, 0))
    
    events = normalize_events(els, operating, end_watching, req)

    e = iter(events)
    periods = zip(e, e)
    r = sum([timediff(s, e, start_watching, end_watching) for s,e in periods])
    #print(f'sum: {r}')
    return r

